public class circle {
    public static void main(String[] args) {
        double area;
        double perimeter;
        double radius = 9.5;
        area = radius * radius * Math.PI;
        perimeter = 2 * radius * Math.PI;

        System.out.println("The perimeter of the circle is: " + perimeter );
        System.out.println("The area of the circle is: " + area );
    }
//
}
